tabby_cat = "\tI'm tabbed in."   # 缩进
persian_cat = "I'm split\non a line." #\n 换行
backslash_cat = "I'm \\ a \\ cat." #\转义\

fat_cat = """         
I'll do a list :
\t\a* Cat food            
\t\b* Fishies           
\t\r* Catnip\n\t* Grass 
"""       #长文本  #\t 缩进 \a 响铃符 \b退格符 \f进纸符 \r回车符

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

